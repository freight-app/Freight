//! Generate a CMake toolchain file that redirects dependency resolution to
//! freight's package prefixes.
//!
//! The file is passed to a foreign CMake build via `-DCMAKE_TOOLCHAIN_FILE`. CMake
//! reads it before `project()`, so it is the earliest, most universal place to put
//! freight's environment: `CMAKE_PREFIX_PATH` / `CMAKE_FIND_ROOT_PATH` are ordered
//! **freight-first**, so when both freight *and* the system provide a library,
//! `find_package` / `find_library` resolve freight's copy — with the sysroot as the
//! genuine fallback. This is what lets a project with many dependencies build against
//! freight's libraries without per-dependency edits.
//!
//! Two modes, driven by whether the build targets a foreign triple:
//! - **native overlay** (no cross triple): find-root mode `BOTH` — freight first, the
//!   host as fallback.
//! - **cross** (a `[target]` triple): set `CMAKE_SYSTEM_NAME`/`PROCESSOR` and use
//!   `ONLY` for libraries/includes so the host environment can never leak in.
//!
//! The on-demand dependency *provider* stays in `Freight.cmake`
//! (`CMAKE_PROJECT_TOP_LEVEL_INCLUDES`) — CMake forbids registering it from a
//! toolchain file.

use std::path::{Path, PathBuf};

/// Per-language toolchain settings for the generated file.
pub struct LangSpec {
    /// CMake language name: `"C"`, `"CXX"`, `"Fortran"`, `"CUDA"`.
    pub cmake_lang: String,
    /// Compiler binary, if freight resolved one for this language.
    pub compiler: Option<String>,
    /// Extra flags for `CMAKE_<lang>_FLAGS_INIT` (space-joined).
    pub flags: Vec<String>,
}

/// CMake wants forward slashes in paths on every platform.
fn cmake_path(p: &Path) -> String {
    p.to_string_lossy().replace('\\', "/")
}

/// Map a freight target OS to a `CMAKE_SYSTEM_NAME` value.
fn cmake_system_name(target_os: &str) -> String {
    match target_os {
        "linux" => "Linux".into(),
        "macos" | "darwin" | "ios" => "Darwin".into(),
        "windows" => "Windows".into(),
        "freebsd" => "FreeBSD".into(),
        "openbsd" => "OpenBSD".into(),
        "netbsd" => "NetBSD".into(),
        "android" => "Android".into(),
        other if !other.is_empty() => {
            let mut c = other.chars();
            c.next()
                .map(|f| f.to_uppercase().collect::<String>() + c.as_str())
                .unwrap_or_default()
        }
        _ => "Generic".into(),
    }
}

/// Write `Freight.toolchain.cmake` into `out_dir` and return its path.
///
/// `langs` carry freight's resolved compiler and any extra flags per language, set
/// before `project()` so the foreign build uses the *same* toolchain (and host-compat
/// flags) as freight's native builds. `cross` is true when building for a foreign
/// target triple — it tightens the find-root modes so nothing leaks from the host.
pub fn generate(
    out_dir: &Path,
    langs: &[LangSpec],
    prefixes: &[PathBuf],
    sysroot: Option<&Path>,
    target_os: &str,
    target_arch: &str,
    cross: bool,
) -> std::io::Result<PathBuf> {
    let mut s = String::new();
    s.push_str(
        "# Generated by freight — uses freight's compilers/flags and redirects\n\
         # dependency resolution to freight's package prefixes. Do not edit.\n\n",
    );

    // Compilers + flags from freight's toolchain, set before project().
    let mut emitted = false;
    for l in langs {
        if let Some(path) = &l.compiler {
            s.push_str(&format!(
                "set(CMAKE_{}_COMPILER \"{}\")\n",
                l.cmake_lang,
                cmake_path(Path::new(path))
            ));
            emitted = true;
        }
        if !l.flags.is_empty() {
            s.push_str(&format!(
                "set(CMAKE_{}_FLAGS_INIT \"{}\")\n",
                l.cmake_lang,
                l.flags.join(" ")
            ));
            emitted = true;
        }
    }
    if emitted {
        s.push('\n');
    }

    // freight prefixes win for config-mode find_package.
    if !prefixes.is_empty() {
        let joined = prefixes
            .iter()
            .map(|p| cmake_path(p))
            .collect::<Vec<_>>()
            .join(";");
        s.push_str(&format!("list(PREPEND CMAKE_PREFIX_PATH \"{joined}\")\n"));
    }

    if let Some(sr) = sysroot {
        s.push_str(&format!("set(CMAKE_SYSROOT \"{}\")\n", cmake_path(sr)));
    }

    if cross {
        s.push_str(&format!(
            "set(CMAKE_SYSTEM_NAME {})\n",
            cmake_system_name(target_os)
        ));
        if !target_arch.is_empty() {
            s.push_str(&format!("set(CMAKE_SYSTEM_PROCESSOR {target_arch})\n"));
        }
    }

    // Ordered find-root: every freight prefix first, then the sysroot. The order is
    // the search priority — freight's copies are found before the system's.
    let mut roots: Vec<String> = prefixes.iter().map(|p| cmake_path(p)).collect();
    if let Some(sr) = sysroot {
        roots.push(cmake_path(sr));
    }
    if !roots.is_empty() {
        let quoted = roots
            .iter()
            .map(|r| format!("\"{r}\""))
            .collect::<Vec<_>>()
            .join(" ");
        s.push_str(&format!("set(CMAKE_FIND_ROOT_PATH {quoted})\n"));
    }

    // Programs (compilers, tools) come from the host. Libraries/includes/packages
    // are searched freight-first; cross builds restrict them to the find-root so the
    // host's libraries never leak into a foreign target.
    let restricted = if cross { "ONLY" } else { "BOTH" };
    s.push_str("set(CMAKE_FIND_ROOT_PATH_MODE_PROGRAM BOTH)\n");
    s.push_str(&format!("set(CMAKE_FIND_ROOT_PATH_MODE_LIBRARY {restricted})\n"));
    s.push_str(&format!("set(CMAKE_FIND_ROOT_PATH_MODE_INCLUDE {restricted})\n"));
    s.push_str(&format!("set(CMAKE_FIND_ROOT_PATH_MODE_PACKAGE {restricted})\n"));

    let path = out_dir.join("Freight.toolchain.cmake");
    std::fs::write(&path, s)?;
    Ok(path)
}

#[cfg(test)]
mod tests {
    use super::*;

    fn lang(name: &str, compiler: Option<&str>, flags: &[&str]) -> LangSpec {
        LangSpec {
            cmake_lang: name.to_string(),
            compiler: compiler.map(str::to_string),
            flags: flags.iter().map(|f| f.to_string()).collect(),
        }
    }

    #[test]
    fn native_overlay_orders_freight_first_and_uses_both() {
        let dir = tempfile::tempdir().unwrap();
        let prefixes = vec![PathBuf::from("/fr/zlib"), PathBuf::from("/fr/png")];
        let langs = vec![
            lang("C", Some("/usr/bin/gcc"), &[]),
            lang("CXX", Some("/usr/bin/g++"), &["-include", "cstdint"]),
        ];
        let path = generate(dir.path(), &langs, &prefixes, None, "linux", "x86_64", false).unwrap();
        let text = std::fs::read_to_string(&path).unwrap();
        assert!(text.contains("set(CMAKE_C_COMPILER \"/usr/bin/gcc\")"));
        assert!(text.contains("set(CMAKE_CXX_COMPILER \"/usr/bin/g++\")"));
        // Host-compat flags land in FLAGS_INIT, space-joined.
        assert!(text.contains("set(CMAKE_CXX_FLAGS_INIT \"-include cstdint\")"));
        assert!(text.contains("list(PREPEND CMAKE_PREFIX_PATH \"/fr/zlib;/fr/png\")"));
        // freight roots appear before any sysroot, with BOTH (host fallback).
        assert!(text.contains("set(CMAKE_FIND_ROOT_PATH \"/fr/zlib\" \"/fr/png\")"));
        assert!(text.contains("MODE_LIBRARY BOTH"));
        assert!(!text.contains("CMAKE_SYSTEM_NAME"));
    }

    #[test]
    fn cross_sets_system_and_restricts_to_find_root() {
        let dir = tempfile::tempdir().unwrap();
        let prefixes = vec![PathBuf::from("/fr/zlib")];
        let sysroot = PathBuf::from("/opt/aarch64-sysroot");
        let path = generate(
            dir.path(),
            &[],
            &prefixes,
            Some(&sysroot),
            "linux",
            "aarch64",
            true,
        )
        .unwrap();
        let text = std::fs::read_to_string(&path).unwrap();
        assert!(text.contains("set(CMAKE_SYSTEM_NAME Linux)"));
        assert!(text.contains("set(CMAKE_SYSTEM_PROCESSOR aarch64)"));
        assert!(text.contains("set(CMAKE_SYSROOT \"/opt/aarch64-sysroot\")"));
        // sysroot is appended after the freight prefix in the find-root.
        assert!(text.contains("set(CMAKE_FIND_ROOT_PATH \"/fr/zlib\" \"/opt/aarch64-sysroot\")"));
        assert!(text.contains("MODE_LIBRARY ONLY"));
        assert!(text.contains("MODE_PROGRAM BOTH"));
    }
}
